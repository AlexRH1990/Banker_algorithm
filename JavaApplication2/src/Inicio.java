public void calcularNecesidad(){
    if (countA == countM) {
        for (int i = 0; i < countA; i++) {
            for (int j = 0; j < 3; j++) {
                Necesidad[i][j] = Max[i][j] - Inicial[i][j];  
            }
        }
        
        DefaultTableModel modelo = (DefaultTableModel) Necesarios.getModel();
        modelo.setRowCount(0); 

        for (int i = 0; i < countA; i++) {
            Object[] fila = { Necesidad[i][0], Necesidad[i][1], Necesidad[i][2] };
            modelo.addRow(fila);
        }
    }
}
  public void ejecutarAlgoritmo() {
   
    System.out.println("Matriz Inicial:");
    imprimirMatriz(Inicial);
    System.out.println("Matriz M치xima:");
    imprimirMatriz(Max);
    System.out.println("Matriz de Disponibilidad:");
    System.out.println("Disponibilidad:");
for (int i = 0; i < Disponibilidad[0].length; i++) {
  System.out.print(Disponibilidad[0][i] + " ");
}
System.out.println();
    System.out.println("Matriz de Necesidades:");
    imprimirMatriz(Necesidad);
    calcularNecesidad();

    
    boolean[] finish = new boolean[countA]; 
    int[] work = Arrays.copyOf(Disponibilidad[0], Disponibilidad[0].length); 

    Arrays.fill(finish, false);

    boolean safe = true;

    
    boolean allFinished = false;
    while (!allFinished) {
        boolean found = false;
        for (int i = 0; i < countA; i++) {
            if (!finish[i]) {
                boolean can_execute = true;
                for (int j = 0; j < 3; j++) {
                    if (Necesidad[i][j] > work[j]) {
                        can_execute = false;
                        break;
                    }
                }
                if (can_execute) {
                    for (int j = 0; j < 3; j++) {
                        work[j] += Inicial[i][j];
                    }
                    finish[i] = true;
                    found = true;
                 
                    respuesta.append("Proceso " + i + " ha sido completado.\n");
                }
            }
        }
        
        allFinished = true;
        for (boolean b : finish) {
            if (!b) {
                allFinished = false;
                break;
            }
        }
        if (!found) {
            safe = false;
            break;
        }
    }

    if (safe) {
        respuesta.append("El sistema es seguro. Todos los procesos han sido completados.\n");
    } else {
        respuesta.append("El sistema est치 en un estado inseguro.\n");
    }
    
}




